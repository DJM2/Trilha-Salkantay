                                    ⮞ Causa rellena: é uma torta fria de purê de batata temperado com limão e pimenta,
                                    recheada com atum, frango, camarão ou legumes. É decorada com ovos cozidos, azeitonas e
                                    maionese.<br>
                                    ⮞ Rocoto relleno: é um prato típico da região de Arequipa e consiste em um pimentão
                                    vermelho picante recheado com carne moída, queijo, ovos e passas. É assado no forno e
                                    servido com batatas gratinadas.<br>
                                    ⮞ Anticuchos: são espetinhos de carne de boi, coração de boi ou frango marinados em
                                    vinagre, alho, pimenta e cominho. São grelhados na brasa e servidos com molho de
                                    pimenta, milho cozido e batata.<br>
                                    ⮞ Picarones: são uma sobremesa típica do Peru e consistem em anéis fritos de massa de
                                    abóbora e batata-doce, cobertos com mel de cana-de-açúcar. São crocantes por fora e
                                    macios por dentro.<br>
                                    ⮞ Papa a la Huancaina: Um prato tradicional à base de batatas cozidas, cobertas com um
                                    molho cremoso de queijo e pimenta amarela. É servido com ovos cozidos, azeitonas e
                                    alface.<br>
                                    ⮞ Causa Limeña: Uma espécie de torta de batata recheada com atum, frango, camarão ou
