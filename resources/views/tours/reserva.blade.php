                                    ⮞ Rocoto Relleno: Um prato picante que consiste em pimentões rocoto recheados com carne
                                    moída, cebola, ovos e azeitonas. É assado no forno e servido com batatas e queijo.
                                    ⮞ Aji de Gallina: Um prato cremoso feito com frango desfiado e cozido em um molho à base
                                    de pimenta amarela, leite, pão e nozes. É servido com arroz e batatas cozidas.<br>
                                    ⮞ Cuy: Também conhecido como "porquinho-da-índia", o cuy é um prato tradicional das
                                    regiões serranas do Peru. O animal é assado ou frito e servido inteiro, muitas vezes
                                    acompanhado de batatas e molho de pimenta.<br>
